/**
 * @file work_light_channel.h
 * @brief Управление одним каналом LED-подсветки рабочей зоны (ШИМ)
 *
 * Класс управляет одним светодиодом (или группой светодиодов) с помощью
 * аппаратного ШИМ-контроллера LEDC. Обеспечивает:
 * - Включение/выключение с запоминанием последней установленной яркости
 * - Установку яркости в диапазоне [0..255] (соответствует 8-битному разрешению)
 * - Конфигурацию токового режима выходного пина (GPIO drive capability)
 * - Опциональное управление выделенным земляным пином (если светодиод требует)
 */

#ifndef WORK_LIGHT_CHANNEL_H
#define WORK_LIGHT_CHANNEL_H

#include <Arduino.h>
#include <stdint.h>
#include <driver/gpio.h>   // gpio_drive_cap_t, gpio_set_drive_capability

#include "config.h"        // LEDC_PWM_FREQ, LEDC_PWM_RESOLUTION

/**
 * @class WorkLightChannel
 * @brief Независимый канал подсветки на базе LEDC ШИМ.
 *
 * Каждый экземпляр управляет собственным пином (и опциональным земляным),
 * использует выделенный канал LEDC. Яркость задаётся 8-битным значением.
 * При выключении (turnOff) запоминается яркость, чтобы при следующем
 * включении (turnOn) восстановить её без повторной установки.
 */
class WorkLightChannel {
public:
    /**
     * @brief Конструктор канала подсветки.
     *
     * @param signalPin   Номер GPIO для ШИМ-управления светодиодом (анод).
     * @param ledcChannel Номер аппаратного LEDC-канала (0..7 для ESP32-S3).
     * @param gndPin      Номер GPIO для земляного вывода (катод, опционально).
     *                    Если не нужен, укажите -1 (по умолчанию).
     * @param driveCap    Токовый режим пина signalPin из enum gpio_drive_cap_t.
     *                    По умолчанию GPIO_DRIVE_CAP_3 (40 мА) для ярких LED.
     */
    WorkLightChannel(uint8_t signalPin,
                     uint8_t ledcChannel,
                     int8_t  gndPin = -1,
                     gpio_drive_cap_t driveCap = GPIO_DRIVE_CAP_3);

    /**
     * @brief Инициализация оборудования: настройка LEDC, пинов, тока.
     *
     * Должна вызываться один раз в setup() до использования канала.
     * - Конфигурирует LEDC-канал с частотой и разрешением из config.h
     * - Подключает signalPin к LEDC и устанавливает drive capability
     * - Если задан gndPin, настраивает его как цифровой выход LOW
     * - Приводит канал в выключенное состояние с яркостью 0
     */
    void begin();

    /**
     * @brief Включить подсветку с последней заданной яркостью.
     *
     * Если яркость была равна 0, светодиод не загорится, но состояние
     * перейдёт в "включено". Для реального включения предварительно
     * задайте ненулевую яркость через setBrightness().
     */
    void turnOn();

    /**
     * @brief Выключить подсветку (ШИМ = 0), запомнив текущую яркость.
     */
    void turnOff();

    /**
     * @brief Задать яркость канала.
     *
     * @param brightness Яркость 0–255 (0 = полностью погашено, 255 = максимум).
     *
     * Новое значение сохраняется и, если канал включён (isOn() == true),
     * немедленно применяется к ШИМ. Если канал выключен, яркость будет
     * применена при следующем turnOn().
     */
    void setBrightness(uint8_t brightness);

    /**
     * @brief Получить текущее сохранённое значение яркости.
     * @return Яркость 0..255, которая будет или была применена.
     */
    uint8_t getBrightness() const;

    /**
     * @brief Проверить, включён ли канал в данный момент.
     * @return true, если канал активен (ШИМ может быть и нулевой яркости).
     */
    bool isOn() const;

private:
    uint8_t _signalPin;          ///< Пин ШИМ (анод)
    uint8_t _ledcChannel;        ///< Аппаратный канал LEDC
    int8_t  _gndPin;             ///< Пин земли (катод) или -1
    gpio_drive_cap_t _driveCap;  ///< Запрошенный токовый режим

    bool    _state;              ///< Включён / выключен
    uint8_t _brightness;         ///< Сохранённая яркость (0..255)

    /**
     * @brief Применить текущее состояние и яркость к ШИМ-выходу.
     *
     * Если _state == true, записывает _brightness; иначе записывает 0.
     */
    void applyPwm();
};

#endif // WORK_LIGHT_CHANNEL_H