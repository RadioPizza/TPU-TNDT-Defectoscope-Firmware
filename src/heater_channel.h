/**
 * @file heater_channel.h
 * @brief Управление одним каналом нагревателя (одно твердотельное реле)
 *
 * Класс контролирует одно реле с защитами:
 * - ограничение максимального времени непрерывного включения
 * - минимальная пауза между выключением и повторным включением
 *
 * Аппаратный переключатель безопасности (при наличии) проверяется снаружи.
 */

#ifndef HEATER_CHANNEL_H
#define HEATER_CHANNEL_H

#include <Arduino.h>
#include <stdint.h>          // uint8_t, uint32_t и т.д.
#include "config.h"

/**
 * @class HeaterChannel
 * @brief Один независимый канал нагревателя.
 *
 * Каждый экземпляр управляет своим реле и хранит свои временные метки.
 * Для работы необходимо вызывать begin() при старте и update() в цикле.
 */
class HeaterChannel {
public:
    /**
     * @brief Конструктор.
     * @param relayPin Номер GPIO, к которому подключено реле.
     */
    HeaterChannel(uint8_t relayPin);

    /**
     * @brief Инициализация аппаратного пина.
     *
     * Должна вызываться один раз в setup(). Настраивает пин как выход
     * и принудительно выключает реле.
     */
    void begin();

    /**
     * @brief Периодическое обновление состояния (проверка защит по времени).
     *
     * Вызывать как можно чаще в loop().
     * - Если реле включено дольше HEATER_MAX_ON_TIME_S, выключает его.
     * - Если прошла пауза HEATER_MIN_PAUSE_S, снимает блокировку включения.
     */
    void update();

    /**
     * @brief Включить реле, если внутренние защиты позволяют.
     * @return true если реле было включено (или уже включено),
     *         false если включение запрещено (активна пауза).
     */
    bool turnOn();

    /**
     * @brief Выключить реле.
     * @return true при успешном выключении (или если уже было выключено).
     *
     * Запускает отсчёт минимальной паузы перед следующим включением.
     */
    bool turnOff();

    /**
     * @brief Проверить, включено ли реле в данный момент.
     * @return true если включено.
     */
    bool isOn() const;

    /**
     * @brief Проверить, можно ли включить реле прямо сейчас (внутренние условия).
     * @return true если канал выключен и пауза истекла.
     *
     * Не учитывает аппаратный переключатель безопасности.
     */
    bool canTurnOn() const;

    /**
     * @brief Возвращает метку времени последнего выключения (мс).
     * @return Время в миллисекундах (millis).
     * @note Используется для отладки и вывода оставшегося времени блокировки.
     */
    uint32_t getTurnOffTime() const;

private:
    uint8_t  _pin;               ///< Номер пина реле
    bool     _state;             ///< Текущее состояние (вкл/выкл)
    uint32_t _turnOnTime;        ///< Метка времени последнего включения (мс)
    uint32_t _turnOffTime;       ///< Метка времени последнего выключения (мс)
    bool     _cooldownActive;    ///< Активна ли пауза после выключения

    /**
     * @brief Непосредственно подать HIGH или LOW на пин реле.
     * @param on true для включения (HIGH), false для выключения (LOW).
     */
    void setRelay(bool on);
};

#endif // HEATER_CHANNEL_H